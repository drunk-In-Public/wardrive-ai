/**
 * AI Route Planner
 * Generates optimal wardriving routes through cold-spot waypoints.
 * Primary: OSRM (road-snapped). Fallback: straight-line with warning.
 */

class RoutePlanner {
  constructor() {
    this.this.osrmBase = "https://router.project-osrm.org/route/v1/driving";
    this.maxWaypoints = 25; // OSRM URL limit
  }

  /**
   * Generate a road-snapped driving route through waypoints via OSRM.
   * @param {Array} waypoints  [{centerLat, centerLon, ...}]
   * @returns {Promise<object>} Route object
   */
  async generateRoute(waypoints) {
    if (waypoints.length === 0) return null;

    const limited = waypoints.slice(0, this.maxWaypoints);

    // Build coordinate string: "lon,lat;lon,lat;..."
    const coordString = limited
      .map((wp) => `${wp.centerLon.toFixed(6)},${wp.centerLat.toFixed(6)}`)
      .join(";");

    const url =
      `${this.osrmBase}/${coordString}` +
      `?overview=full&geometries=geojson&steps=true&annotations=false`;

    console.log("[RoutePlanner] Requesting OSRM route:", url);

    let resp;
    try {
      resp = await fetch(url, { signal: AbortSignal.timeout(20000) });
    } catch (fetchErr) {
      throw new Error(`OSRM fetch failed: ${fetchErr.message}`);
    }

    if (!resp.ok) {
      const text = await resp.text().catch(() => "");
      throw new Error(`OSRM HTTP ${resp.status}: ${text.slice(0, 120)}`);
    }

    let data;
    try {
      data = await resp.json();
    } catch {
      throw new Error("OSRM returned non-JSON response");
    }

    if (data.code !== "Ok") {
      throw new Error(`OSRM error: ${data.code} — ${data.message || ""}`);
    }

    if (!data.routes || data.routes.length === 0) {
      throw new Error("OSRM returned no routes");
    }

    const route = data.routes[0];
    console.log(
      `[RoutePlanner] Road route: ${(route.distance / 1000).toFixed(1)} km,`,
      `${Math.round(route.duration / 60)} min,`,
      `${route.geometry.coordinates.length} road points`
    );

    return {
      waypoints: limited,
      geometry: route.geometry, // GeoJSON LineString — full road path
      totalDistanceKm: route.distance / 1000,
      totalDurationMin: route.duration / 60,
      isRoadRoute: true,
      legs: (route.legs || []).map((leg, i) => ({
        index: i,
        from: limited[i],
        to: limited[i + 1],
        distanceKm: leg.distance / 1000,
        durationMin: leg.duration / 60,
        steps: (leg.steps || []).map((step) => ({
          instruction: step.maneuver?.type || "continue",
          modifier: step.maneuver?.modifier || "",
          name: step.name || "",
          distanceM: step.distance,
        })),
      })),
    };
  }

  /**
   * Straight-line fallback route when OSRM is unavailable.
   * Connects waypoints in order with straight segments.
   */
  generateFallbackRoute(waypoints) {
    const coords = waypoints.map((wp) => [wp.centerLon, wp.centerLat]);
    return {
      waypoints,
      geometry: { type: "LineString", coordinates: coords },
      totalDistanceKm: this._estimateDistance(waypoints),
      totalDurationMin: null,
      isRoadRoute: false, // straight-line fallback
      legs: [],
    };
  }

  /**
   * Export route as GPX for GPS devices / navigation apps (Organic Maps, Garmin, etc.)
   */
  exportGPX(route, name = "WardDrive Route") {
    const now = new Date().toISOString();

    const wpts = route.waypoints
      .map(
        (wp, i) => `  <wpt lat="${wp.centerLat.toFixed(6)}" lon="${wp.centerLon.toFixed(6)}">
    <name>Cold Spot ${i + 1}</name>
    <desc>Score: ${(wp.score * 100).toFixed(0)}% | Existing scans: ${wp.count}</desc>
  </wpt>`
      )
      .join("\n");

    const trkpts = (route.geometry?.coordinates || [])
      .map(([lon, lat]) => `      <trkpt lat="${lat.toFixed(6)}" lon="${lon.toFixed(6)}"/>`)
      .join("\n");

    return `<?xml version="1.0" encoding="UTF-8"?>
<gpx version="1.1" creator="WardDrive AI"
  xmlns="http://www.topografix.com/GPX/1/1"
  xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance"
  xsi:schemaLocation="http://www.topografix.com/GPX/1/1 http://www.topografix.com/GPX/1/1/gpx.xsd">
  <metadata>
    <name>${name}</name>
    <time>${now}</time>
    <desc>Wardriving route targeting low-scan-density areas. Generated by WardDrive AI.</desc>
  </metadata>
${wpts}
  <trk>
    <name>${name}</name>
    <trkseg>
${trkpts}
    </trkseg>
  </trk>
</gpx>`;
  }

  _estimateDistance(waypoints) {
    let total = 0;
    for (let i = 1; i < waypoints.length; i++) {
      total += this._haversine(
        waypoints[i - 1].centerLat, waypoints[i - 1].centerLon,
        waypoints[i].centerLat,    waypoints[i].centerLon
      );
    }
    return total;
  }

  _haversine(lat1, lon1, lat2, lon2) {
    const R = 6371;
    const dLat = ((lat2 - lat1) * Math.PI) / 180;
    const dLon = ((lon2 - lon1) * Math.PI) / 180;
    const a =
      Math.sin(dLat / 2) ** 2 +
      Math.cos((lat1 * Math.PI) / 180) *
        Math.cos((lat2 * Math.PI) / 180) *
        Math.sin(dLon / 2) ** 2;
    return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  }
}

window.RoutePlanner = RoutePlanner;
